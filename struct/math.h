#ifndef MATH_TYPES_H
#define MATH_TYPES_H

#include <stdint.h>

// 1 Byte
typedef uint8_t byte;

typedef int8_t signed_byte;
typedef int8_t sbyte;

// 2 Bytes
typedef short int short_int;
typedef short int sint;
typedef short int shint;

typedef unsigned short int unsigned_short;
typedef unsigned short int ushort;

// 4 Bytes
typedef unsigned int unsigned_int;
typedef unsigned int uint;

// 8 Bytes
typedef long unsigned int long_int;
typedef long unsigned int lint;

typedef long int signed_long;
typedef long int slint;
typedef long int slong;

#endif
